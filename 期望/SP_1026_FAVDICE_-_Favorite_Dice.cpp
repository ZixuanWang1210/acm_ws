void sol(){
    int n; cin>>n;
    double dp[n+1];
    dp[n]=0;
    for(int i=n-1;i>=0;i--){
        dp[i]=dp[i+1]+1.0*n/(n-i);
    }
    cout<<fixed<<setprecision(2)<<dp[0]<<endl;

}

